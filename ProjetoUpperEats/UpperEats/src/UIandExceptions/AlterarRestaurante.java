            Repositorio.desserializar();
            // TODO add your handling code here:
            Repositorio.getInstance().alteraRestaurante(Integer.parseInt(numeroRestaurante.getText()), dono, jTxtMorada.getText(), jTxtNome.getText(),tipoCozinha1.getText(), nTelefone.getText(), localidade.getText());
